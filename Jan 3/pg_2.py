''' write a program to find difference of 2 numbers '''

a = int(input('ENTER 1st NUMBER: ')) # getting 1st input and stores in a variable called 'a'
b = int(input('ENTER 2nd NUMBER: ')) # getting 2st input and stores in a variable called 'b'

diff = a - b # finding difference between the values stored in 'a' and 'b' and stores in a  variable called 'diff'

print('difference = ' , diff) # printing the difference
